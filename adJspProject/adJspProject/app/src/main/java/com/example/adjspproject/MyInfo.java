package com.example.adjspproject;

public class MyInfo {
    String userID, userPassword, userName, userGender, userEmail;

    public MyInfo() {
    }

    public MyInfo(String userID, String userPassword, String userName, String userGender, String userEmail) {

        this.userID = userID;
        this.userPassword = userPassword;
        this.userName = userName;
        this.userGender = userGender;
        this.userEmail = userEmail;
    }
}
